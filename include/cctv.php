<h3>دوربین مداربسته :</h3>
<p>
امروزه در مراکز صنعتی و اداری جهت نظارت بر محیط فیزیکی و نظارت برکار کارکنان یاکـارگران در جـهت کنترل و مدیـریت بهـتر و کارآمـد‌تر بـه وفـور از سیستمهای تـلویزیونی مـداربسته  (closed circuit TV)(cctv) استفاده می‌شود.این سیستمها به عنوان سیستمهای کنترل تصویری نیز نامیده می‌شوند. گاهی نیز از این سیستمها با مخفف CCVE (تجهیزات ویدئویی مدار بسته) یاد می‌شود. در محلهایی مانند بانکها ـ ادارات ـ دانشگاهها ـ کارخانجات ـ فروشگاههای بزرگ ـ فروشگاههای فروش اجناس گرانقیمت مانند طلافروشیها ـ در سوپرمارکتهای بزرگ  و در کنترل ترافیک خیابانها و چهارراه‌ها این سیستمها را میتوان نصب و مورد استفاده قرار داد. استفاده از این سیستمها در منازل مسکونی رواج چندانی نیافته است ولی با پا به عرصه گذاشتن سیستمهای تصویری که قادرند حرکت را در محدودة تحت نظارت سیستم تشخیص و اعلام خطر نمایند یا توسط سنسورهای خاصی تحریک شده و شروع به ضبط فیلم از محل بنمایند انتظار می‌رود که استفاده از این سیستمها در منازل مسکونی نیز گسترش بیاید.  

به اینگونه سیستمها هم اکنون اصطلاح دزدگیر تصویری اطلاق می‌شود. اصول کار سیستمهای CCTV به این صورت است که ابتدا تصاویر توسط دوربینهای مدار بسته دریافت شده و برای نمایش و پخش به مانیتور یا تلویزیون انتقال داده می‌شود. همچنین برای ضبط و یا تغییر نحوه نمایش روی مانیتور و پخش همزمان تصاویر دوربینها روی مانیتور و کنترل از راه دور دوربینها نیز تجهیزات و امکاناتی وجود دارد. چون تصاویر دریافت شده از این سیستمها برای بینندگان محدودی می‌باشد لذا به آنها تلویزیون مدار بسته می‌گویند بر خلاف تلویزیون عمومی (Broadcast TV) که جهت پخش تصاویر برای عموم می‌باشد. با توجه به تنظیماتی که روی دوربینها و سایر تجهیزات میتوان انجام داد این سیستمها در شرایط جوی متفاوت و در روز و شب نیز کارآیی خوبی دارند.

برای کنترل ورود و خروج افراد به یک محل و برای کنترل مکانهای وسیع توسط چندین دوربین و نمایش همزمان تصویر آنها و نظارت سمعی و بصری از فواصل بسیار دور از طریق شبکه تلفن بدون نیاز به حضور فیزیکی کنترل کننده در محل و در دستگاههایی که کنترل بصری آنها توسط انسان مقدور نبوده یا خطر آفرین می‌باشد نیز میتوان از این سیستمها استفاده کرد.
</p>
